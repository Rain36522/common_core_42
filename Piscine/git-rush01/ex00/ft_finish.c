int	ft_finish_check(char array[6][6])
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (j < 5)
	{
		if (i == 5)
		{
			j ++;
			i = 0;
		}
		else
		{
			if (!(array[j][i]))
				return (0);
		}
		i ++;
	}
	return (1);
}
