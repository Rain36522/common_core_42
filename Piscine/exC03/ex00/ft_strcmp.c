#include <unistd.h>

/*si s1 plus grand que s2 valeure positive*/

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1  && *s1 == *s2)
	{
		s1 ++;
		s2 ++;
	}
	return (*s1 - *s2);
}
